    private void kButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton2ActionPerformed

        String ad = ad_alani.getText();
        String soyad = soyad_alani.getText();
        String dept = dept_alani.getText();
        String email = email_alani.getText();
        int telNo = Integer.valueOf(tel_alani.getText());     
        int maas = Integer.valueOf(maas_alani.getText());
         
        int selectedrow = calisanTable.getSelectedRow();
        
        if(selectedrow == -1){
            
            if(model.getRowCount() == 0){
                
                uyariCalisan.setText("Calisanlar Tablosu Boş!");
                
            }
            else{
                
                uyariCalisan.setText("Lütfen Güncellenecek Bir Çalışan Seçiniz!");
                
            }
            
        }
        else{
            
            int id = (int)model.getValueAt(selectedrow, 0);
            islemler.calisanGuncelle(id,ad,soyad,dept,email,telNo,maas);
            calisanGoruntule();
            
            uyariCalisan.setText("Çalışan Başarıyla Güncellendi!");
            
        }
    }//GEN-LAST:event_kButton2ActionPerformed

