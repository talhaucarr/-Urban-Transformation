    private void yikmaEkleBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yikmaEkleBActionPerformed

        uyariCalisan.setText("");
        
        String ad = ad_alani.getText();
        String soyad = soyad_alani.getText();
        String dept = dept_alani.getText();
        String email = email_alani.getText();
        int telNo = Integer.valueOf(tel_alani.getText());        
        int maas = Integer.valueOf(maas_alani.getText());
        
        islemler.calisanEkle(ad,soyad,dept,email,telNo,maas);
        if(islemler.bayrak == 0){
            
            jLabel9.setText("Böyle bir firma bulunamıyor!");
            
        }
        
        calisanGoruntule();
        
        uyariCalisan.setText("Yeni Çalışan Başarıyla Eklendi...");
    }//GEN-LAST:event_yikmaEkleBActionPerformed

    private void yikmaYedekBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yikmaYedekBActionPerformed
        File file = new File("CalisanlarYedek.txt");//proje içinde text.txt adında bir txt oluşturun.
        
        ArrayList<Calisan> calisanlar = new ArrayList<Calisan>();
        
        calisanlar = islemler.calisanlariGetir();
        
        if(calisanlar != null){
                        
             try(BufferedWriter br = new BufferedWriter(new FileWriter(file))){
                            for(Calisan calisan : calisanlar){
                            br.write(calisan.getAd() + " " + calisan.getSoyad() + " " + calisan.getDepartman()+ " " + calisan.getEmail() + " " + calisan.getTelNo() + " " + calisan.getMaas() );
                            br.newLine();
                            }
                        }

                        catch (IOException e) {
                            System.out.println("Unable to read file " +file.toString());
                    }                          
        }      
    }//GEN-LAST:event_yikmaYedekBActionPerformed

